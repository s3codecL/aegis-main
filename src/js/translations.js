// Translation helper function
export function t(key, lang = null) {
  const currentLang = lang || localStorage.getItem("osintLanguage") || "es";
  return translations[currentLang] && translations[currentLang][key] ? translations[currentLang][key] : key;
}

// Translations object for incident management pages
export const Translations = {
  currentLanguage: localStorage.getItem("osintLanguage") || "es",

  setLanguage: function (lang) {
    this.currentLanguage = lang;
    localStorage.setItem("osintLanguage", lang);
    this.applyTranslations();
  },

  applyTranslations: function () {
    const lang = this.currentLanguage;

    // Update page title
    document.title = translations[lang]["INCIDENTS_PAGE_TITLE"] || "Gesti√≥n de Incidencias - Aegis Dashboard";

    // Update all elements with data-i18n
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      const translation = translations[lang][key];
      if (translation) {
        el.innerHTML = translation;
      }
    });

    // Update all placeholders with data-i18n-placeholder
    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      const key = el.getAttribute("data-i18n-placeholder");
      const translation = translations[lang][key];
      if (translation) {
        el.placeholder = translation;
      }
    });
  }
};

// Backward compatibility
window.translations = translations;
